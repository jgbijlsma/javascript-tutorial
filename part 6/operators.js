// operators

// assignment operator: =

let price = 6;
price = 10;

// arithmetic operators: + - * / += -= *= /= ++ --

let num1 = 2;
let num2 = 5;
let result;

// addition: +
result = num1 + num2;
console.log(result);

// subtraction: -

result = num1 - num2;
console.log(result);

// multiplication: *

result = num1 * num2;
console.log(result);

// division: /

result = num1 / num2;
console.log(result);

// shorthand operators

let count = 1;
count += 5;
console.log(count);
count -= 5;
console.log(count);
count *= 5;
console.log(count);
count /= 5;
console.log(count);

count = 10;
count++;
console.log(count);
count--;
console.log(count);

// string operators

let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName;
console.log(fullName);
fullName = fullName - "Doe";
console.log(fullName);
