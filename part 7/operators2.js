// comparison operators

let val1 = 4;
let val2 = 5;
let result;

// equal

result = val1 == val2;
console.log(result);

// not equal

result = val1 != val2;
console.log(result);

// less than

result = val1 < val2;
console.log(result);

// more than

result = val1 > val2;
console.log(result);

// less or equal

result = val1 <= val2;
console.log(result);

// more or equal

result = val1 >= val2;
console.log(result);

// comparing data types

val1 = 10;
val2 = "10";

result = val1 == val2;
console.log(result);

result = val1 === val2;
console.log(result);
