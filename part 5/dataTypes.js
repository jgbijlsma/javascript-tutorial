// data types

// numbers

let n = 5;
console.log(n);

let inf = 1 / 0;
console.log(inf);

let notANumber = 1 / "Some text";
console.log(notANumber);

// strings

let str = "Some text";
console.log(str);

// boolean

let loggedIn = true;
console.log(loggedIn);

// undefined

let undef;
console.log(undef);

// null

let noValue = null;
console.log(noValue);

// typeof operator

let whatIsTheType = typeof undef;
console.log(whatIsTheType);

// weak typed

let x = 1;
console.log(x);
x = "Something else";
console.log(x);
